package com.itheima;

public class Opretor {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        //加法
        int res1 = a + b ;
        //减法
        int res2 = a - b ;
        //乘法
        int res3 = a * b ;
        //除法
        int res4 = a / b ;
        //取模
        int res5 = a % b ;
        //测试
        int res6 = a + b ;
        System.out.println(res1);
        System.out.println(res2);
        System.out.println(res3);
        System.out.println(res4);
        System.out.println(res5);
        System.out.println(res6);
    }
}
